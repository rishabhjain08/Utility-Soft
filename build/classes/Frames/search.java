    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ESCAPE||jTextField1.getText().equals("")||evt.getKeyCode()==KeyEvent.VK_ENTER)
            dispose();
        table.removeRowSelectionInterval(0, table.getRowCount()-1);
        int i=0;
        CharSequence s;
        s=jTextField1.getText().toLowerCase().subSequence(0, jTextField1.getText().length());
         while(table.getValueAt(i, 0)!=null){
            if(((String)table.getValueAt(i, 1)).toLowerCase().contains(s)){
                table.setRowSelectionInterval(i, i);
         table.scrollRectToVisible(table.getCellRect(i, 0, true));
            break;
//                System.exit(0);
             }
            i++;
        }
            
    }//GEN-LAST:event_jTextField1KeyReleased

